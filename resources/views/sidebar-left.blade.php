        <h1> - Computadoras centrales y dedicadas</h1>
      <img class="imgr borderedbox inspace-5" src="../img/Central.jpg" alt="">
      <p>Computadora central</p>
      <p>Una computadora central (en inglés mainframe) es una computadora grande, potente y costosa, usada principalmente por una gran compañía para el procesamiento de una gran cantidad de datos; por ejemplo, para el procesamiento de transacciones bancarias</p>
      <p>¿Qué es un servidor dedicado?</p>
      <p>Un servidor dedicado es un equipo informático físico que destina todos sus recursos a proporcionar información y atender las peticiones de otro ordenador (cliente) que ha contratado sus servicios. Por tanto, a diferencia de un servidor compartido, el dedicado trabaja en exclusiva para un solo cliente. Existe un amplio abanico de servidores dedicados entre los que poder elegir para el alojamiento en Internet.</p>
